export function generateMockAreaData() {
  const months = ["Jan", "Mar", "May", "Jul", "Sep", "Nov"];
  const startYear = 2020;
  const numPoints = 42;

  let year = startYear;
  let monthIndex = 0;
  let y = 100;
  const data = [];

  for (let i = 0; i < numPoints; i++) {
    const x = `${months[monthIndex]}${String(year).slice(2)}`;
    data.push({ x, y });
    monthIndex++;
    if (monthIndex >= months.length) {
      monthIndex = 0;
      year++;
    }
    y += 25;
  }

  const lastPoint = data[data.length - 1];
  const lastMonth = lastPoint.x.slice(0, 3);
  const lastYear = parseInt("20" + lastPoint.x.slice(3), 10);
  let nextMonthIndex = months.indexOf(lastMonth) + 1;
  let nextYear = lastYear;
  if (nextMonthIndex >= months.length) {
    nextMonthIndex = 0;
    nextYear++;
  }
  const nextX = `${months[nextMonthIndex]}${String(nextYear).slice(2)}`;
  data.push({ x: nextX, y: lastPoint.y });

  return data;
}

